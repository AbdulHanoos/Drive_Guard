def measure_inference_speed(model, dataset, num_batches=30):
    total_time = 0.0
    total_images = 0
    for images, labels in dataset.take(num_batches):
        start_time = time.time()
        _ = model.predict(
            images,
            verbose=0
        )
        end_time = time.time()
        total_time += end_time - start_time
        total_images += images.shape[0]
    avg_time_per_image = total_time / total_images
    fps = 1.0 / avg_time_per_image
    return avg_time_per_image, fps
