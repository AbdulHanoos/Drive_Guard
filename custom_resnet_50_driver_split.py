def build_resnet50(input_shape, num_classes):
    """
    Build an improved ResNet50-style model for
    driver distraction classification.
    """

    # Input image
    X_input = Input(
        shape=input_shape,
        name="input_image"
    )

    # Apply augmentation only during training
    X = data_augmentation(X_input)

    # ---------------------------------------------
    # Stage 1: Initial convolution and max pooling
    # ---------------------------------------------
    X = ZeroPadding2D(
        padding=(3, 3)
    )(X)

    X = Conv2D(
        filters=64,
        kernel_size=(7, 7),
        strides=(2, 2),
        name="conv1",
        kernel_initializer=glorot_uniform(seed=0)
    )(X)

    X = BatchNormalization(
        axis=3,
        name="bn1"
    )(X)

    X = Activation("relu")(X)

    X = MaxPooling2D(
        pool_size=(3, 3),
        strides=(2, 2)
    )(X)

    # ---------------------------------------------
    # Stage 2
    # ---------------------------------------------
    X = convolutional_block(
        X,
        f=3,
        filters=[64, 64, 256],
        stage=2,
        block="a",
        s=1
    )

    X = identity_block(
        X,
        3,
        [64, 64, 256],
        stage=2,
        block="b"
    )

    X = identity_block(
        X,
        3,
        [64, 64, 256],
        stage=2,
        block="c"
    )

    # ---------------------------------------------
    # Stage 3
    # ---------------------------------------------
    X = convolutional_block(
        X,
        f=3,
        filters=[128, 128, 512],
        stage=3,
        block="a",
        s=2
    )

    X = identity_block(X, 3, [128, 128, 512], stage=3, block="b")
    X = identity_block(X, 3, [128, 128, 512], stage=3, block="c")
    X = identity_block(X, 3, [128, 128, 512], stage=3, block="d")

    # ---------------------------------------------
    # Stage 4
    # ---------------------------------------------
    X = convolutional_block(
        X,
        f=3,
        filters=[256, 256, 1024],
        stage=4,
        block="a",
        s=2
    )

    X = identity_block(X, 3, [256, 256, 1024], stage=4, block="b")
    X = identity_block(X, 3, [256, 256, 1024], stage=4, block="c")
    X = identity_block(X, 3, [256, 256, 1024], stage=4, block="d")
    X = identity_block(X, 3, [256, 256, 1024], stage=4, block="e")
    X = identity_block(X, 3, [256, 256, 1024], stage=4, block="f")

    # ---------------------------------------------
    # Stage 5
    # ---------------------------------------------
    X = convolutional_block(
        X,
        f=3,
        filters=[512, 512, 2048],
        stage=5,
        block="a",
        s=2
    )

    X = identity_block(X, 3, [512, 512, 2048], stage=5, block="b")
    X = identity_block(X, 3, [512, 512, 2048], stage=5, block="c")

    # ---------------------------------------------
    # Classification head
    # ---------------------------------------------
    X = GlobalAveragePooling2D(
        name="global_average_pooling"
    )(X)

    X = Dense(
        units=512,
        activation="relu",
        kernel_initializer=glorot_uniform(seed=0),
        name="dense_512"
    )(X)

    X = Dropout(
        rate=0.5,
        name="dropout"
    )(X)

    X = Dense(
        units=num_classes,
        activation="softmax",
        name="output",
        kernel_initializer=glorot_uniform(seed=0)
    )(X)

    model = Model(
        inputs=X_input,
        outputs=X,
        name="ResNet50_Improved"
    )

    return model

