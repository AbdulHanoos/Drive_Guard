    image_bgr = cv2.imread(image_path)

    if image_bgr is None:
        raise FileNotFoundError(
            f"Could not load image: {image_path}"
        )

    # Convert BGR to RGB to match the training pipeline
    image_rgb = cv2.cvtColor(
        image_bgr,
        cv2.COLOR_BGR2RGB
    )

    # Resize and normalize exactly as done during training
    resized = cv2.resize(
        image_rgb,
        (IMG_SIZE, IMG_SIZE)
    )

    processed = resized.astype("float32") / 255.0

    # Add batch dimension:
    # (128, 128, 3) → (1, 128, 128, 3)
    processed = np.expand_dims(
        processed,
        axis=0
    )

    # Predict class probabilities
    preds = model.predict(
        processed,
        verbose=0
    )

    pred_idx = int(
        np.argmax(preds[0])
    )

    confidence = float(
        preds[0][pred_idx]
    ) * 100

    pred_name = CLASS_NAMES[pred_idx]

    if show:
        # Extract actual class folder from validation-image path
        actual_code = os.path.basename(
            os.path.dirname(image_path)
        )

        actual_idx = CLASS_CODES.get(
            actual_code,
            -1
        )

        actual_name = (
            CLASS_NAMES[actual_idx]
            if actual_idx >= 0
            else "Unknown"
        )

        # Green title for correct result, red for incorrect result
        colour = (
            "green"
            if pred_idx == actual_idx
            else "red"
        )

        plt.figure(figsize=(5, 5))

        plt.imshow(image_rgb)

        plt.title(
            f"Actual: {actual_name}\n"
            f"Predicted: {pred_name} "
            f"({confidence:.1f}%)",
            color=colour,
            fontsize=11
        )

        plt.axis("off")
        plt.show()

    return pred_name, confidence


