def identity_block(X, f, filters, stage, block):
    """
    Identity block:
    The input and output dimensions remain the same.
    The shortcut path is added directly without resizing.
    """

    conv_name = "res" + str(stage) + block + "_"
    bn_name = "bn" + str(stage) + block + "_"

    f1, f2, f3 = filters
    bn_axis = 3

    # Save the original input for the skip connection
    X_skip = X

    # Main path: Component 1
    X = Conv2D(
        filters=f1,
        kernel_size=(1, 1),
        strides=(1, 1),
        padding="valid",
        name=conv_name + "1",
        kernel_initializer=glorot_uniform(seed=0)
    )(X)

    X = BatchNormalization(
        axis=bn_axis,
        name=bn_name + "1"
    )(X)

    X = Activation("relu")(X)

    # Main path: Component 2
    X = Conv2D(
        filters=f2,
        kernel_size=(f, f),
        strides=(1, 1),
        padding="same",
        name=conv_name + "2",
        kernel_initializer=glorot_uniform(seed=0)
    )(X)

    X = BatchNormalization(
        axis=bn_axis,
        name=bn_name + "2"
    )(X)

    X = Activation("relu")(X)

    # Main path: Component 3
    X = Conv2D(
        filters=f3,
        kernel_size=(1, 1),
        strides=(1, 1),
        padding="valid",
        name=conv_name + "3",
        kernel_initializer=glorot_uniform(seed=0)
    )(X)

    X = BatchNormalization(
        axis=bn_axis,
        name=bn_name + "3"
    )(X)

    # Add main path and shortcut path
    X = Add()([X, X_skip])

    X = Activation("relu")(X)

    return X


def convolutional_block(X, f, filters, stage, block, s=2):
    """
    Convolutional block:
    The input and output dimensions are different.
    A 1×1 convolution is applied to the shortcut path
    so that its dimensions match the main path.
    """

    conv_name = "res" + str(stage) + block + "_"
    bn_name = "bn" + str(stage) + block + "_"

    f1, f2, f3 = filters
    bn_axis = 3

    # Save the original input for the skip connection
    X_skip = X

    # Main path: Component 1
    X = Conv2D(
        filters=f1,
        kernel_size=(1, 1),
        strides=(s, s),
        padding="valid",
        name=conv_name + "1",
        kernel_initializer=glorot_uniform(seed=0)
    )(X)

    X = BatchNormalization(
        axis=bn_axis,
        name=bn_name + "1"
    )(X)

    X = Activation("relu")(X)

    # Main path: Component 2
    X = Conv2D(
        filters=f2,
        kernel_size=(f, f),
        strides=(1, 1),
        padding="same",
        name=conv_name + "2",
        kernel_initializer=glorot_uniform(seed=0)
    )(X)

    X = BatchNormalization(
        axis=bn_axis,
        name=bn_name + "2"
    )(X)

    X = Activation("relu")(X)

    # Main path: Component 3
    X = Conv2D(
        filters=f3,
        kernel_size=(1, 1),
        strides=(1, 1),
        padding="valid",
        name=conv_name + "3",
        kernel_initializer=glorot_uniform(seed=0)
    )(X)

    X = BatchNormalization(
        axis=bn_axis,
        name=bn_name + "3"
    )(X)

    # Shortcut path: resize dimensions using 1×1 convolution
    X_skip = Conv2D(
        filters=f3,
        kernel_size=(1, 1),
        strides=(s, s),
        padding="valid",
        name=conv_name + "skip",
        kernel_initializer=glorot_uniform(seed=0)
    )(X_skip)

    X_skip = BatchNormalization(
        axis=bn_axis,
        name=bn_name + "skip"
    )(X_skip)

    # Add main path and shortcut path
    X = Add()([X, X_skip])

    X = Activation("relu")(X)

    return X

