class SimplePreprocessor:
    """Resize image to a fixed width x height."""
    def __init__(self, width, height, inter=cv2.INTER_AREA):
        self.width  = width
        self.height = height
        self.inter  = inter

    def preprocess(self, image):
        return cv2.resize(image, (self.width, self.height), interpolation=self.inter)


class ImageToArrayPreprocessor:
    """Convert a PIL/cv2 image to a Keras-compatible numpy array."""
    def __init__(self, data_format=None):
        self.data_format = data_format

    def preprocess(self, image):
        return img_to_array(image, data_format=self.data_format)


class SimpleDatasetLoader:
    """Load images from a list of paths, apply preprocessors, return (data, labels)."""
    def __init__(self, preprocessors=None):
        self.preprocessors = preprocessors if preprocessors is not None else []

    def load(self, image_paths, verbose=500):
        data, labels = [], []
        for i, img_path in enumerate(image_paths):
            image = cv2.imread(img_path)

            if image is None:
                print("Skipped unreadable image:", img_path)
                continue
            image = cv2.imread(img_path)
            label = img_path.split(os.path.sep)[-2]
            for p in self.preprocessors:
                image = p.preprocess(image)
            data.append(image)
            labels.append(label)
            if verbose > 0 and (i + 1) % verbose == 0:
                print(f'[INFO] processed {i+1}/{len(image_paths)}')
        return np.array(data), np.array(labels)

